create database trgovina;
use trgovina;

create table trgovac(
    sifra int NOT NULL auto_increment PRIMARY KEY,
    ime varchar(50) NOT NULL,
    prezime varchar(50) NOT NULL,
    OIB char(11) NOT NULL,
    adresa varchar(50),
    kontakt varchar(50) NOT NULL
)engine=innodb;
create table kategorijaProizvoda(
    sifra int NOT NULL auto_increment PRIMARY KEY,
    naziv varchar(50) not null,
    PDV decimal(18,2) NOT NULL
)engine=innodb;
create table proizvodac(
    sifra int not null AUTO_INCREMENT PRIMARY KEY,
    naziv varchar(50) not null,
    sjediste varchar(50),
    godina_osnutka int
)engine=innodb;
create table proizvod(
    sifra int not null auto_increment PRIMARY KEY,
    naziv varchar(50) not null,
    proizvodac int not null,
    kategorija_proizvoda int not null,
    cijena decimal(18,2) not null,
    zaliha int 
)engine=innodb;
create table transakcija(
    sifra int not null auto_increment PRIMARY KEY,
    prodavac int not null,
    vrijeme datetime not null
)engine=innodb;
create table proizvodTransakcija(
    transakcija int not null,
    proizvod int not null,
    kolicina int not null 
)engine=innodb;


alter table proizvod add FOREIGN key(proizvodac) references proizvodac(sifra);
alter table proizvod add FOREIGN key(kategorija_proizvoda) references kategorijaProizvoda(sifra);

alter table transakcija add foreign key(proizvodac) references trgovac(sifra);
alter table transakcija add foreign key(proizvod) references proizvod(sifra);

